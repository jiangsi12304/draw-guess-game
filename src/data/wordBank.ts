export function getRandomWord(difficulty?: 'easy' | 'normal' | 'hard'): WordItem {
  let filtered = wordBank;
  if (difficulty) {
    filtered = wordBank.filter(w => w.difficulty === difficulty);
  }
  return filtered[Math.floor(Math.random() * filtered.length)];
}

export function getRandomWords(count: number, difficulty?: 'easy' | 'normal' | 'hard'): WordItem[] {
  const words: WordItem[] = [];
  for (let i = 0; i < count; i++) {
    words.push(getRandomWord(difficulty));
  }
  return words;
}
